from google.adk.agents import LlmAgent
from google.adk.models.google_llm import Gemini
from google.adk.tools.agent_tool import AgentTool
from google.adk.tools.google_search_tool import google_search
from google.genai import types
from typing import List

# Retry configuration
retry_config = types.HttpRetryOptions(
    attempts=5,
    exp_base=7,
    initial_delay=1,
    http_status_codes=[429, 500, 503, 504],
)

# ---- Helper function to filter tutorials/courses ----
def filter_resources(results: List[str]) -> List[str]:
    """Filter search results to keep only tutorials or courses."""
    return [
        r for r in results
        if "tutorial" in r.lower() or "course" in r.lower() or "guide" in r.lower()
    ]

# ---- Google Search Agent ----
google_search_agent = LlmAgent(
    name="google_search_agent",
    model=Gemini(model="gemini-2.5-flash-lite", retry_options=retry_config),
    description="Searches for learning resources using Google search",
    instruction="Use the google_search tool to find tutorials, guides, or courses on the topic.",
    tools=[google_search],
)

# ---- Root Agent ----
learning_resource_agent = LlmAgent(
    name="learning_resource_agent",
    model=Gemini(model="gemini-2.5-flash-lite", retry_options=retry_config),
    instruction="""
Your task is to find learning resources (tutorials, guides, courses) on the user-provided topic.

Steps:
1) Use 'google_search_agent' to find resources.
2) Pass the results to 'filter_resources' to keep only tutorials/guides/courses.
3) Return both the curated list and the total number of resources.
""",
    tools=[AgentTool(agent=google_search_agent), filter_resources],
)

print("‚úÖ Learning Resource Agent created")

